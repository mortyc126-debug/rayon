class GF2Poly:
    """Multilinear polynomial over GF(2).
    Represented as set of monomials (each monomial = frozenset of variable indices).
    """
    def __init__(self, monomials=None):
        self.terms = set()
        if monomials:
            for m in monomials:
                m = frozenset(m)
                if m in self.terms:
                    self.terms.remove(m)  # 1 + 1 = 0 in GF(2)
                else:
                    self.terms.add(m)

    def __add__(self, other):
        result = GF2Poly()
        result.terms = self.terms.symmetric_difference(other.terms)
        return result

    def __mul__(self, other):
        result = GF2Poly()
        for m1 in self.terms:
            for m2 in other.terms:
                product = m1 | m2  # union = product of variables (x·x = x in GF2)
                if product in result.terms:
                    result.terms.remove(product)
                else:
                    result.terms.add(product)
        return result

    def degree(self):
        if not self.terms:
            return -1
        return max(len(m) for m in self.terms)

    def is_zero(self):
        return len(self.terms) == 0

    def leading_term(self, var_order):
        """Leading term under graded reverse lex order."""
        if not self.terms:
            return None
        return max(self.terms, key=lambda m: (len(m), tuple(sorted(m, key=lambda v: -var_order.get(v, v)))))

    def __repr__(self):
        if not self.terms:
            return "0"
        parts = []
        for m in sorted(self.terms, key=lambda m: (len(m), sorted(m))):
            if len(m) == 0:
                parts.append("1")
            else:
                parts.append("·".join(f"v{v}" for v in sorted(m)))
        return " + ".join(parts)

    def copy(self):
        p = GF2Poly()
        p.terms = set(self.terms)
        return p


def var(i):
    """Create polynomial for variable i."""
    p = GF2Poly()
    p.terms.add(frozenset([i]))
    return p


def const(c):
    """Create constant polynomial."""
    p = GF2Poly()
    if c % 2 == 1:
        p.terms.add(frozenset())
    return p


def build_gate_ideal(n, gates):
    """Build the ideal generators from gate equations.

    Variables: 0..n-1 = inputs, n..n+s-1 = gate outputs.
    """
    generators = []

    # Field equations: vᵢ² = vᵢ → vᵢ² + vᵢ = 0
    # In multilinear ring, these are automatic (we only use multilinear polys)

    for gi, (gtype, inp1, inp2, out) in enumerate(gates):
        v_out = var(out)
        v1 = var(inp1)

        if gtype == 'AND':
            v2 = var(inp2)
            # out = inp1 · inp2 → out + inp1·inp2 = 0
            gen = v_out + v1 * v2
            generators.append(gen)

        elif gtype == 'OR':
            v2 = var(inp2)
            # out = inp1 + inp2 + inp1·inp2 → out + inp1 + inp2 + inp1·inp2 = 0
            gen = v_out + v1 + v2 + v1 * v2
            generators.append(gen)

        elif gtype == 'NOT':
            # out = 1 + inp1 → out + inp1 + 1 = 0
            gen = v_out + v1 + const(1)
            generators.append(gen)

    return generators


def compute_groebner_degree(generators, num_vars):
    """Compute max degree in a Gröbner basis (simplified).

    This is a VERY simplified Gröbner basis computation.
    For small instances only.

    Returns: max degree of basis elements.
    """
    if not generators:
        return 0

    # Simple Buchberger's algorithm over GF(2) multilinear ring
    basis = [g.copy() for g in generators if not g.is_zero()]
    max_deg = max(g.degree() for g in basis) if basis else 0

    # Variable ordering
    var_order = {i: i for i in range(num_vars)}

    # Reduce: for each pair, compute S-polynomial and reduce
    # Simplified: just track max degree of generated polys
    changed = True
    iterations = 0
    max_iterations = 100  # safety limit

    while changed and iterations < max_iterations:
        changed = False
        iterations += 1
        new_polys = []

        for i in range(len(basis)):
            for j in range(i+1, len(basis)):
                # S-polynomial (simplified for GF(2))
                lt_i = basis[i].leading_term(var_order)
                lt_j = basis[j].leading_term(var_order)

                if lt_i is None or lt_j is None:
                    continue

                # LCM of leading terms
                lcm = lt_i | lt_j

                # S-poly = (lcm/lt_i) * basis[i] + (lcm/lt_j) * basis[j]
                # Over GF(2): multiplier = product of vars in lcm \ lt
                mult_i = GF2Poly([lcm - lt_i]) if lcm != lt_i else const(1)
                mult_j = GF2Poly([lcm - lt_j]) if lcm != lt_j else const(1)

                s_poly = mult_i * basis[i] + mult_j * basis[j]

                # Reduce s_poly by basis (simplified)
                if not s_poly.is_zero():
                    deg = s_poly.degree()
                    if deg > max_deg:
                        max_deg = deg
                        changed = True
                    # Add to basis if non-trivial
                    if len(new_polys) < 50:  # limit basis size
                        new_polys.append(s_poly)

        basis.extend(new_polys)

    return max_deg


def build_and_analyze(n, gates, func_name):
    """Build trajectory ideal and compute its properties."""
    s = len(gates)
    num_vars = n + s

    generators = build_gate_ideal(n, gates)

    # Basic stats
    degrees = [g.degree() for g in generators]
    num_terms = [len(g.terms) for g in generators]

    print(f"\n  {func_name}:")
    print(f"    n={n}, s={s}, vars={num_vars}")
    print(f"    {len(generators)} generators")
    print(f"    Generator degrees: {degrees}")
    print(f"    Generator terms: {num_terms}")
    print(f"    Max generator degree: {max(degrees) if degrees else 0}")

    # Compute Gröbner basis degree (proxy for regularity)
    gb_deg = compute_groebner_degree(generators, num_vars)
    print(f"    Gröbner basis max degree: {gb_deg}")
    print(f"    Regularity estimate: {gb_deg}")

    return gb_deg


