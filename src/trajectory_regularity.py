"""
STEP 1 OF RESEARCH PROGRAM: Compute regularity of trajectory ideals.

The trajectory ideal I(T(C)) is generated by gate-consistency polynomials
over GF(2)[x₁,...,xₙ, g₁,...,gₛ] / (xᵢ² - xᵢ, gⱼ² - gⱼ).

Working in the Boolean polynomial ring: every variable satisfies v² = v.

Gate equations:
  AND: gᵢ - gⱼ·gₖ = 0          (equivalently: gᵢ + gⱼ·gₖ over GF(2))
  OR:  gᵢ - gⱼ - gₖ + gⱼ·gₖ = 0  (equivalently: gᵢ + gⱼ + gₖ + gⱼ·gₖ)
  NOT: gᵢ - 1 + gⱼ = 0          (equivalently: gᵢ + gⱼ + 1)

Output constraint: gₛ - f(x) = 0 (last gate = target function).
But f(x) is given as truth table, not polynomial. Convert via
multilinear representation over GF(2).

The REGULARITY of I measures the maximum degree of generators
in a MINIMAL free resolution of the quotient ring.

For practical computation: use the degree of the Gröbner basis
elements as a proxy for regularity.
"""

