"""
TRAJECTORY SPACE: The space of computational paths.

A circuit C of size s on n inputs defines a map:
  τ: {0,1}^n → {0,1}^{n+s}
  τ(x) = (x₁,...,xₙ, g₁(x),...,gₛ(x))

The IMAGE of τ is the TRAJECTORY SPACE T(C) ⊆ {0,1}^{n+s}.

T(C) is constrained by CONSISTENCY EQUATIONS:
  For each AND gate: gᵢ = gⱼ ∧ gₖ
  For each OR gate:  gᵢ = gⱼ ∨ gₖ
  For each NOT gate: gᵢ = ¬gⱼ

These are POLYNOMIAL equations over GF(2):
  AND: gᵢ = gⱼ · gₖ        (degree 2)
  OR:  gᵢ = gⱼ + gₖ - gⱼ·gₖ (degree 2)
  NOT: gᵢ = 1 - gⱼ          (degree 1)

T(C) = solution set of s polynomial equations in n+s variables.

THE KEY: T(C) is an ALGEBRAIC VARIETY over GF(2).
Its dimension = n (parameterized by n input variables).
Its DEGREE and other algebraic invariants capture circuit complexity.

NEW MEASURE: The ALGEBRAIC COMPLEXITY of T(C).

For a function f: define the minimum algebraic complexity of
any trajectory space computing f:
  AC(f) = min over circuits C computing f:
          (degree of ideal defining T(C))

If AC(CLIQUE) = super-poly: circuit_size ≥ ??? (need to relate AC to size).

THE RELATIONSHIP:
  Circuit of size s → s equations of degree ≤ 2.
  The ideal I(T(C)) is generated by s polynomials of degree ≤ 2.
  The DEGREE of the variety V(I) ≤ 2^s (Bezout's theorem).

  If AC(f) = degree of V(I): AC ≤ 2^s → s ≥ log AC. LOGARITHMIC AGAIN?

  BUT: the variety lives in an (n+s)-dimensional ambient space.
  The actual degree depends on how the s equations INTERACT.

  For INDEPENDENT equations: degree = 2^s.
  For DEPENDENT equations (as in a circuit): degree is LESS.

  The ACTUAL degree of a circuit's trajectory space:
  how the consistency equations constrain the trajectories.

EXPERIMENT: Compute trajectory spaces for small circuits and
measure their algebraic properties.
"""

