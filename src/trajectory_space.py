def compute_trajectory_space(n, gates):
    """Compute T(C) = all valid trajectories.

    Each trajectory: (x₁,...,xₙ, g₁,...,gₛ) satisfying all gate equations.
    """
    s = len(gates)
    total_wires = n + s

    trajectories = []
    for bits in range(2**n):
        x = tuple((bits >> j) & 1 for j in range(n))

        # Compute gate values
        wire_vals = list(x) + [0] * s
        for gi, (gtype, inp1, inp2, _) in enumerate(gates):
            if gtype == 'AND':
                wire_vals[n + gi] = wire_vals[inp1] & wire_vals[inp2]
            elif gtype == 'OR':
                wire_vals[n + gi] = wire_vals[inp1] | wire_vals[inp2]
            elif gtype == 'NOT':
                wire_vals[n + gi] = 1 - wire_vals[inp1]

        trajectories.append(tuple(wire_vals))

    return trajectories


def trajectory_hamming_distances(trajectories):
    """Compute pairwise Hamming distances between trajectories."""
    dists = []
    for i in range(len(trajectories)):
        for j in range(i+1, len(trajectories)):
            d = sum(a != b for a, b in zip(trajectories[i], trajectories[j]))
            dists.append(d)
    return dists


def trajectory_dimension(trajectories, n_plus_s):
    """Estimate dimension of trajectory space via GF(2) rank.

    The trajectories form a set in {0,1}^{n+s}.
    The "affine dimension" = rank of the difference matrix.
    """
    if len(trajectories) <= 1:
        return 0

    # Take differences from first trajectory
    base = trajectories[0]
    diff_matrix = []
    for t in trajectories[1:]:
        diff = tuple((a ^ b) for a, b in zip(base, t))
        diff_matrix.append(list(diff))

    # GF(2) rank
    m = len(diff_matrix)
    nc = n_plus_s
    mat = [row[:] for row in diff_matrix]

    rank = 0
    for col in range(nc):
        pivot = None
        for row in range(rank, m):
            if mat[row][col]:
                pivot = row
                break
        if pivot is None:
            continue
        mat[rank], mat[pivot] = mat[pivot], mat[rank]
        for row in range(m):
            if row != rank and mat[row][col]:
                for c in range(nc):
                    mat[row][c] ^= mat[rank][c]
        rank += 1

    return rank


def trajectory_correlation_structure(trajectories, n, s):
    """Analyze how gate values correlate with each other and with inputs.

    In the trajectory space: wires are CORRELATED (gate values depend on inputs).
    The CORRELATION MATRIX captures the structure.
    """
    total_wires = n + s
    num_traj = len(trajectories)

    if num_traj <= 1:
        return {}

    # Compute mean of each wire
    means = [sum(t[w] for t in trajectories) / num_traj for w in range(total_wires)]

    # Compute correlation between pairs of wires
    # For GF(2): correlation = Pr[w₁ = w₂] - 0.5
    high_corr_pairs = 0
    total_pairs = 0

    for w1 in range(total_wires):
        for w2 in range(w1+1, total_wires):
            agree = sum(1 for t in trajectories if t[w1] == t[w2])
            corr = abs(agree / num_traj - 0.5) * 2  # normalize to [0,1]
            total_pairs += 1
            if corr > 0.5:
                high_corr_pairs += 1

    return {
        'high_corr_fraction': high_corr_pairs / total_pairs if total_pairs > 0 else 0,
        'total_wires': total_wires,
    }


def compute_trajectory_boundary(trajectories, n, s):
    """Compute the "boundary" of the trajectory space.

    Two trajectories are "adjacent" if they differ on exactly one INPUT bit.
    The boundary = pairs of adjacent trajectories where output differs.

    This is the BOUNDARY OF f lifted to the trajectory space.
    """
    total_wires = n + s
    output_idx = n + s - 1  # last gate is output

    boundary_pairs = []
    traj_by_input = {t[:n]: t for t in trajectories}

    for t in trajectories:
        x = t[:n]
        for j in range(n):
            # Flip input bit j
            x_flipped = list(x)
            x_flipped[j] = 1 - x_flipped[j]
            x_flipped = tuple(x_flipped)

            if x_flipped in traj_by_input:
                t2 = traj_by_input[x_flipped]
                if t[output_idx] != t2[output_idx]:
                    # Boundary pair! Count how many WIRE values differ
                    wire_diffs = sum(a != b for a, b in zip(t, t2))
                    boundary_pairs.append(wire_diffs)

    return boundary_pairs


